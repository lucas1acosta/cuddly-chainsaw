package modelo;

class Veiculo {

  final String placa;
  final String marca;
  final String modelo;
  final int    cilindrada;

  Veiculo(String placa, String marca, String modelo, int cilindrada) {
    this.placa      = placa;
    this.marca      = marca;
    this.modelo     = modelo;
    this.cilindrada = cilindrada;
  }

}
